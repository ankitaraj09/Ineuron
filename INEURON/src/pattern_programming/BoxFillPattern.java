package pattern_programming;

public class BoxFillPattern {
	public static void main(String args[]) {
		int n=5;
		for(int i=0;i<n;i++)                            // data type of i and j declare krna compulsory
		{
			for(int j=0;j<n;j++)
			{
				System.out.print("*");               //System.out.println("*");  vertical star print
			}
			 System.out.println();                   // for jump into next row
			
		}
		
		
	}

}



